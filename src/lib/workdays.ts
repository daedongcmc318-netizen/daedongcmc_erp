function fmtISO(d: Date): string {
  return `${d.getFullYear()}-${String(d.getMonth() + 1).padStart(2, "0")}-${String(d.getDate()).padStart(2, "0")}`;
}

export function isWorkday(d: Date): boolean {
  const day = d.getDay();
  if (day === 0 || day === 6) return false;
  if (KOREAN_HOLIDAYS.has(fmtISO(d))) return false;
  return true;
}

/**
 * 다음 영업일 반환 (자기 자신 다음 날부터 시작해서 평일/공휴일 건너뜀)
 * 안전장치: 30일 이내 못 찾으면 그냥 +1일 반환
 */
export function nextWorkday(d: Date): Date {
  const x = new Date(d.getFullYear(), d.getMonth(), d.getDate());
  for (let i = 0; i < 30; i++) {
    x.setDate(x.getDate() + 1);
    if (isWorkday(x)) return x;
  }
  // fallback
  const fb = new Date(d);
  fb.setDate(fb.getDate() + 1);
  return fb;
}
